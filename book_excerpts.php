		<td class="maintext" colspan="2">
			<img src="images/nietzsche.gif" width="95" height="136" alt="Nietzsche" border="1" align="left" />When the oppressed, downtrodden, outraged exhort one another with the vengeful cunning of impotence: &quot;let us be different from the evil, namely good! And he is good who does not outrage, who harms nobody, who does not attack, who does not requite, who leaves revenge to God, who keeps himself hidden as we do, who avoids evil and desires little from life, like us, the patient, humble, and just&quot; --this, listened to calmly and without previous bias, really amounts to no more than: &quot;we weak ones are, after all, weak; it would be good if we did nothing <i>for which we are not strong enough</i>&quot;; but this dry matter of fact, this prudence of the lowest order which even insects posess (posing as dead, when in great danger, so as not to do &quot;too much&quot;), has, thanks to the counterfeit and self-deception of impotence, clad itself in the ostentatious garb of the virtue of quiet, calm resignation, just as if the weakness of the weak--that is to say, their <i>essence</i>, their effects, their sole ineluctable, irremovable reality--were a voluntary achievement, willed, chosen, a <i>deed</i>, a <i>meritorious</i> act.  This type of man <i>needs</i> to believe in a neutral independent &quot;subject,&quot; prompted by an instinct for self-preservation and self-affirmation in which every lie is sanctified.  The subject (or, to use a more popular expression, the <i>soul</i>) has perhaps been believed in hitherto more firmly than anything else on earth because it makes possible to the majority of mortals, the weak and oppressed of any kind, the sublime self-deception that interprets weakness as freedom, and their being thus-and-thus as a <i>merit</i>.

			<p align="center"><b>14</b></p>

			<p>Would anyone like to take a look into the secret of how <i>ideals are made</i> on earth?  Who has the courage?-- Very well!  Here is a point we can see through this dark workshop,  But wait a moment or two, Mr. Rash and Curious: your eyes must first get used to this false iridescent light.-- All right!  Now speak!  What is going on down there?  Say what you see, man of the most perilous kind of inquisitiveness--now I am the one who is listening.--</p>
			
			<p>--&quot;I see nothing, but I hear the more.  There is a soft, wary, malignant muttering and whispering coming from all the corners and nooks.  It seems to me one is lying; a saccharine sweetness clings to every sound.  Weakness is being lied into something <i>meritorious</i>, no doubt of it--so it is just as you said&quot;--</p>
			
			<p>--Go on!</p>
			
			<p>--&quot;and impotence which does not requite into 'goodness of heart'; anxious lowliness into 'humility'; subjection to those one hates into 'obedience' (that is, to one of whom they say commands this subjection--they call him God).  The inoffensiveness of the weak man, even the cowardice of which he has so much, his lingering at the door, his being ineluctably compelled to wait, here acquire the flattering names, such as 'patience,' and are even called virtue itself; his inability for revenge is called unwillingness to revenge, perhaps even forgiveness ('for <i>they</i> know not what they do--we alone know what <i>they</i> do!').  They also speak of 'loving one's enemies'--and sweat as they do so.&quot;</p>
			
			<p>--Go on!</p>
			
			<p>--&quot;They are miserable, no doubt of it, all these mutterers and nook counterfeiters, although they crouch warmly together--but they tell me their misery is a sign of being chosen by God; one beats the dog one likes best; perhaps this misery is also a preparation, a testing, a schooling, perhaps it is even more--something that will one day be made good and recompensated with interest, with huge payments of gold, no! of happiness.  This they call 'bliss.'&quot;</p>
			
			<p>--Go on!</p>
			
			<p>--&quot;Now they give me to understand that they are not merely better than the mighty, the lords of the earth whose spittle they have to lick (<i>not</i> from fear, not at all from fear! but because God has commanded them to obey the authorties)--that they are not merely better but also 'better off,' or at least will be better off someday.  But enough! enough! I can't take any more.  Bad air!  Bad air!  This workshop where <i>ideals are manufactured</i>--it seems to me it stinks of so many lies.&quot;</p>
			
			<p>--No! Wait a moment!  You have said nothing yet of the masterpiece of these black magicians, who make whiteness, milk, and innocence of every blackness--haven't you noticed their perfection of refinement, their boldest, subtlest, most ingenious, most mendacious artistic stroke?  Attend to them!  These cellar rodents full of vengefulness and hatred--what have they made of revenge and hatred?  Have you heard these words uttered?  If you trusted simply to their words, would you suspect you were among men of <i>ressentiment</i>? ...</p>
