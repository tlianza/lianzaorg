	<img src="images/jhj.gif" width="125" height="152" alt="James Hervey Johnson" border="1" align="left" />Intelligent men do not decide any subject until they have carefully examined both or all sides of it. Fools, cowards, and those too lazy to think, accept blindly, without examination, dogmas and doctrines imposed upon them in childhood by their parents, priests, and teachers, when their minds were immature and they could not reason.

<p>[Some] 433,000,000 Mohammedans believe that the Koran was brought by
an angel from heaven; 335,000,000 Hindus believe one of their gods, Siva,
has six arms; 153,000,000 Buddhists believe they will be reincarnated;
904,000,000 Christians believe a god made the world in six days, Joshua
stopped the sun by yelling at it, and Jesus was born of a virgin and nullified
natural laws to perform miracles.</p>

<p>There is absolutely no scientific proof of any of these claims. Science
has shown them to be contrary to all known facts. It is more intelligent
to classify them as false. Religions are all based upon the primitive superstitions of ignorant, stone-age men who had no knowledge of science and thought the world was flat. The Catholic Church imprisoned Galileo for life and burned Bruno at the stake because they disagreed with these superstitious beliefs.</p>

<p>These primitive beliefs have been kept alive by a vast army of priests,
preachers, and rabbis because it is to their great profit to promote them,
first, by imposing them on the helpless brains of children, and second,
by saturating the air, TV, press, and schools with their childish superstitions
and unreasonable claims. They fool the ignorant and make the gullible and
the intelligent alike pay tribute to them. Their multi-billion-dollar
properties and incomes are exempt from taxes; they get half-fare on
trains, busses, and planes; and receive billions of dollars in grants of
taxpayers' money to help build up their political power, wealth, and luxurious
living. Taxes could be cut 10 percent if churches paid their just share.
That would mean a probable saving of 20 billion dollars a year to the people
of the U.S. every year of their lives. Some priests also indoctrinated
with superstition from childhood probably believe what they preach. It
pays them handsomely to do so.</p>

<p>Religious beliefs are against common sense. There is no god, just because
priests say so. There are no angels, devils, heavens, hells, ghosts, witches,
nor miracles. These superstitious beliefs are promoted for the purpose
of making the gullible believe that by paying money to the priest-class,
they will be favored by one of the gods. There is nothing supernatural
-- nothing contrary to natural law.</p>

<p>Religion has caused untold ignorance, murder, torture, fear, poverty,
unhappiness, wars, and has kept the world 10,000 years behind the times.
It still does, while the millions support the priestly loafers in comfort
and ease. For ages the independent thinkers have been murdered, ostracized,
tortured, and suppressed and their writings destroyed. Only in recent years
have a few courageous thinkers been free to criticize religions.</p>

<p>Great thinkers and scientists -- Voltaire, Thomas Paine, Charles Bradlaugh,
Luther Burbank, Thomas Jefferson, Thomas Edison, Charles Darwin, Albert
Einstein, Henry L. Mencken, Charles Smith, Joseph Lewis, Rupert Hughes,
Bertrand Russell, H.G. Wells, Mark Twain, Herbert Spencer, Thomas Huxley,
Clarence Darrow, Chapman Cohen, George McDonald, George Bernard Shaw, and
hundreds of others -- have discarded all or most of the religious beliefs.</p>

<p>This leaflet will be shocking to the unfortunate victim brain-whipped
by religious indoctrination from childhood. But those who have a spark
of intelligence will examine the facts, will stop paying tribute to the
religious profiteers, and lose their fear of a mythical god and mythical
hell.</p>

<p>If the gods which foolish people pray to were decent beings they would
not permit innocent children to die of cancer, be blind, suffer from polio,
muscular dystrophy, syphilis. A good god would not have manufactured fleas,
bedbugs, chiggers, lice, rattlesnakes, sharks, deadly germs, sickness,
diseased brains, idiots, and insanity. All these things are the result
of blind, natural evolution. A just god would not cause some innocent people
to die or be disabled for life in airplane, train, and ship disasters while
others survived.</p>

<p>The <b>Thinkers Club</b> appeals to you to examine both sides so we
may all escape from this religious oppression which degenerates the minds,
forces all to pay tribute to the priestly parasites, and retards human
progress.</p>
